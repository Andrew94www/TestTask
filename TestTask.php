/* Вариант № 1 : Агоритм подсччета для квадрата 3 x 3:
1 шаг: 3*(3+2+1)
2 шаг: 2*(3+2+1)
3 шаг: 1*(3+2+1)
4 шаг: Сумируем предедущие шаги
*/
function countRectangle($m,$n){   
  $result = 0;
  for ($i = $m; $i >= 1; $i--) {
    for ($k = $n; $k >= 1; $k--) {
      $result  =  $result+ $i*$k;
    }
}
 return $result;
} 
